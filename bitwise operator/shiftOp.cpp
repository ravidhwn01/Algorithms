#include <iostream>
#include <bits/stdc++.h>
using namespace std;
int main()
{
    
    cout <<"(17 << 1) = "<<(17 >>1) << endl;
    cout <<"(17 << 2) = "<<(17 >>2) << endl;
    cout <<"(19 << 1) = "<<(19 <<1) << endl;
    cout <<"(21 << 2) = "<<(21 <<2) << endl;

//     (17 << 1) = 8 
//     (17 << 2) = 4 
//     (19 << 1) = 38
//     (21 << 2) = 84
    return 0;
}