#include <iostream>
#include <bits/stdc++.h>
using namespace std;
int main()
{
    int a = 12;
    int b = 5;
    cout <<"(a & b) = "<<(a & b) << endl;
    cout <<"(a ^ b) = "<<(a ^ b) << endl;
    cout <<"(a | b) = "<<(a | b) << endl;
    cout <<"~b = "<<~b << endl;
    cout <<"~a = "<<~a << endl;
// (a & b) = 4
// (a ^ b) = 9
// (a | b) = 13
// ~b = -6
// ~a = -13
    return 0;
}